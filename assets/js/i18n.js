const SUPPORTED = LANGS.map((l) => l.code);
const DIR_BY_LANG = LANGS.reduce((acc, l) => { acc[l.code] = l.dir; return acc; }, {});

function detectInitialLang() {
  const saved = localStorage.getItem("dd_lang");
  if (saved && SUPPORTED.includes(saved)) return saved;
  // Honour the browser's ordered language preferences; fall back to English.
  const prefs = (navigator.languages && navigator.languages.length)
    ? navigator.languages
    : [navigator.language || "en"];
  for (const pref of prefs) {
    const base = String(pref).toLowerCase().split("-")[0];
    if (SUPPORTED.includes(base)) return base;
  }
  return "en";
}

function applyLang(lang) {
  if (!SUPPORTED.includes(lang)) lang = "en";
  const dict = I18N[lang] || I18N.en;

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    const val = dict[key] !== undefined ? dict[key] : I18N.en[key];
    if (val !== undefined) el.textContent = val;
  });

  document.querySelectorAll("[data-i18n-attr]").forEach((el) => {
    // format: "attr:key;attr2:key2"
    el.getAttribute("data-i18n-attr").split(";").forEach((pair) => {
      const [attr, key] = pair.split(":");
      const val = dict[key] !== undefined ? dict[key] : I18N.en[key];
      if (attr && key && val !== undefined) el.setAttribute(attr, val);
    });
  });

  // Long legal blocks: privacy/terms are only written in en + pl, so any other
  // language falls back to the English text instead of showing nothing.
  const legalEls = document.querySelectorAll("[data-lang]");
  if (legalEls.length) {
    const available = new Set(
      Array.from(legalEls).map((el) => el.getAttribute("data-lang"))
    );
    const legalLang = available.has(lang) ? lang : "en";
    legalEls.forEach((el) => {
      el.style.display = el.getAttribute("data-lang") === legalLang ? "" : "none";
    });
  }

  document.documentElement.setAttribute("lang", lang);
  document.documentElement.setAttribute("dir", DIR_BY_LANG[lang] || "ltr");
  localStorage.setItem("dd_lang", lang);

  document.querySelectorAll(".lang-select").forEach((sel) => {
    if (sel.value !== lang) sel.value = lang;
  });
  // Legacy EN/PL button toggle, kept for backwards compatibility.
  document.querySelectorAll(".lang-toggle button").forEach((btn) => {
    btn.classList.toggle("active", btn.getAttribute("data-set-lang") === lang);
  });
}

function buildLangSelectors() {
  document.querySelectorAll(".lang-select").forEach((sel) => {
    if (sel.dataset.built === "1") return;
    LANGS.forEach((l) => {
      const opt = document.createElement("option");
      opt.value = l.code;
      opt.textContent = l.name;
      sel.appendChild(opt);
    });
    sel.dataset.built = "1";
    sel.addEventListener("change", () => applyLang(sel.value));
  });
}

document.addEventListener("DOMContentLoaded", () => {
  buildLangSelectors();
  applyLang(detectInitialLang());

  document.querySelectorAll(".lang-toggle button").forEach((btn) => {
    btn.addEventListener("click", () => applyLang(btn.getAttribute("data-set-lang")));
  });

  // mobile nav
  const toggle = document.querySelector(".nav-toggle");
  const links = document.querySelector(".nav-links");
  if (toggle && links) {
    toggle.addEventListener("click", () => links.classList.toggle("open"));
    links.querySelectorAll("a").forEach((a) =>
      a.addEventListener("click", () => links.classList.remove("open"))
    );
  }

  // scroll reveal
  const io = new IntersectionObserver(
    (entries) => {
      entries.forEach((e) => {
        if (e.isIntersecting) {
          e.target.classList.add("in");
          io.unobserve(e.target);
        }
      });
    },
    { threshold: 0.12 }
  );
  document.querySelectorAll(".reveal").forEach((el) => io.observe(el));

  // inspiration carousel
  const carousel = document.querySelector(".inspo-carousel");
  if (carousel) {
    const slides = Array.from(carousel.querySelectorAll(".inspo-slide"));
    const dotsWrap = carousel.querySelector(".inspo-dots");
    let current = 0;
    let timer = null;

    slides.forEach((_, i) => {
      const dot = document.createElement("button");
      dot.className = "inspo-dot" + (i === 0 ? " active" : "");
      dot.setAttribute("role", "tab");
      dot.setAttribute("aria-label", "Slide " + (i + 1));
      dot.addEventListener("click", () => { goTo(i); resetTimer(); });
      dotsWrap.appendChild(dot);
    });

    function goTo(n) {
      slides[current].classList.remove("active");
      dotsWrap.children[current].classList.remove("active");
      current = (n + slides.length) % slides.length;
      slides[current].classList.add("active");
      dotsWrap.children[current].classList.add("active");
    }

    function startTimer() { timer = setInterval(() => goTo(current + 1), 4000); }
    function resetTimer() { clearInterval(timer); startTimer(); }

    carousel.querySelector(".inspo-prev").addEventListener("click", () => { goTo(current - 1); resetTimer(); });
    carousel.querySelector(".inspo-next").addEventListener("click", () => { goTo(current + 1); resetTimer(); });

    carousel.addEventListener("mouseenter", () => clearInterval(timer));
    carousel.addEventListener("mouseleave", startTimer);

    // touch / swipe support
    let touchX = null;
    carousel.addEventListener("touchstart", (e) => { touchX = e.touches[0].clientX; }, { passive: true });
    carousel.addEventListener("touchend", (e) => {
      if (touchX === null) return;
      const dx = e.changedTouches[0].clientX - touchX;
      if (Math.abs(dx) > 40) { goTo(current + (dx < 0 ? 1 : -1)); resetTimer(); }
      touchX = null;
    }, { passive: true });

    slides[0].classList.add("active");
    startTimer();
  }
});
